package CursoDeJava.Apps.Aula05;

public class Aula05 {
    public static void main(String[] args){
        for(int i=0;i<=10;i++){
            System.out.printf("%s%d%n","Olá",i);
        }

        int i = 0;
        while(i<=10){
            System.out.printf("%s%d%n","Olá",i);
            i++;
        }
        int j = 0;
        do{
            System.out.printf("%s%d%n","Olá",j);
            j++;
        }while(j<=10);



    }
    
}
